package org.iptime.mypj;

public class StudentInfoVO {
    private int idx;
    private String nm;
    private int aty;
    private String tdy;

    public int getIdx() {
        return idx;
    }

    public void setIdx(int idx) {
        this.idx = idx;
    }

    public String getNm() {
        return nm;
    }

    public void setNm(String nm) {
        this.nm = nm;
    }

    public int getAty() {
        return aty;
    }

    public void setAty(int aty) {
        this.aty = aty;
    }

    public String getTdy() {
        return tdy;
    }

    public void setTdy(String tdy) {
        this.tdy = tdy;
    }
}
